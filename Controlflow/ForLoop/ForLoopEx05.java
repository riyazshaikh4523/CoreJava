package forloopassignment;
//11. Positive Integers: Create a program that prints all the positive  
//integers less than 20 using a `for` loop.
public class ForLoopEx05 {

	public static void main(String[] args) {
		int a=1;
		int b=20;
		for(a=1;a<b;a++) {
			if (a>0 && a<20) {
				System.out.println(a);
			}
			
		
		}
	}

}
