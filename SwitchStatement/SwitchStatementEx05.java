package switchstatement;
//Grades: Design a program that takes a student's score as input (0-100) and
//outputs the corresponding letter grade (A, B, C, D, F) using a switch-case
//statement.
public class SwitchStatementEx05 {

	public static void main(String[] args) {
		int a=1;
		switch(a) {
		
		case 1:
			System.out.println("A");
			break;
		case 2:
			System.out.println("B");
			break;
		case 3:
			System.out.println("c");
			break;
		case 4:
			System.out.println("D");
			break;
		case 5:
			System.out.println("F");
		}
	}

}
